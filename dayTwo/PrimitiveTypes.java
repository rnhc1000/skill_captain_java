package dayTwo;

public class PrimitiveTypes {

    public static void main(String[] args) {

        byte b = 127;
        char c = 'c';
        short r = 32767;
        int i = 65536;
        long l = 1L;
        float f = 1.0f;
        double d = 1000d;
        boolean t = true;

        System.out.println(
                b + " " +
                c + " " +
                r + " " +
                i + " " +
                l + " " +
                f + " " +
                d + " " +
                t + " "

        );

    }

}
